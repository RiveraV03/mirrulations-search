    document_id VARCHAR(50) NOT NULL PRIMARY KEY,
    docket_id VARCHAR(50) NOT NULL,
    document_api_link VARCHAR(2000) NOT NULL UNIQUE,
    address1 VARCHAR(200),
    address2 VARCHAR(200),
    agency_id VARCHAR(20) NOT NULL,
    is_late_comment BOOLEAN,
    author_date TIMESTAMP WITH TIME ZONE,
    comment_category VARCHAR(200),
    city VARCHAR(100),
    comment TEXT,
    comment_end_date TIMESTAMP WITH TIME ZONE,
    comment_start_date TIMESTAMP WITH TIME ZONE,
    country VARCHAR(100),
    document_type CHAR(30) NOT NULL,
    effective_date TIMESTAMP WITH TIME ZONE,
    email VARCHAR(320),
    fax VARCHAR(20),
    flex_field1 TEXT,
    flex_field2 TEXT,
    first_name VARCHAR(100),
    submitter_gov_agency VARCHAR(300),
    submitter_gov_agency_type VARCHAR(50),
    implementation_date TIMESTAMP WITH TIME ZONE,
    last_name VARCHAR(100),
    modify_date TIMESTAMP WITH TIME ZONE NOT NULL,
    is_open_for_comment BOOLEAN DEFAULT FALSE,
    submitter_org VARCHAR(200),
    phone VARCHAR(40),
    posted_date TIMESTAMP WITH TIME ZONE,
    postmark_date TIMESTAMP WITH TIME ZONE,
    reason_withdrawn VARCHAR(1000),
    receive_date TIMESTAMP WITH TIME ZONE,
    reg_writer_instruction TEXT,
    restriction_reason VARCHAR(1000),
    restriction_reason_type VARCHAR(100),
    state_province_region VARCHAR(100),
    subtype VARCHAR(100),
    document_title VARCHAR(500),
    topics VARCHAR(250)[],
    is_withdrawn BOOLEAN DEFAULT FALSE,
    postal_code VARCHAR(10),
    frdocnum VARCHAR(50),
    attachments_self_link    VARCHAR(2000),
    attachments_related_link VARCHAR(2000),
    file_formats             JSONB,
    display_properties       JSONB
);

-- =========================================
-- COMMENTS TABLE
-- =========================================
-- documents can have multiple comments, but comments are not required for documents
-- comment_id is generated by the application and is not auto-incrementing

CREATE TABLE IF NOT EXISTS comments (
    comment_id VARCHAR(50) NOT NULL PRIMARY KEY,
    api_link VARCHAR(2000) NOT NULL UNIQUE,
    document_id VARCHAR(50) REFERENCES documentsWithFRdoc(document_id),
    duplicate_comment_count INT DEFAULT 0 NOT NULL,
    address1 VARCHAR(200),
    address2 VARCHAR(200),
    agency_id VARCHAR(20) NOT NULL,
    city VARCHAR(100),
    comment_category VARCHAR(200),
    comment TEXT, -- public comment submission text
    country VARCHAR(100),
    docket_id VARCHAR(50) REFERENCES dockets(docket_id),
    document_type VARCHAR(100) NOT NULL,
    email VARCHAR(320),
    fax VARCHAR(20),
    flex_field1 TEXT,
    flex_field2 TEXT,
    first_name VARCHAR(100),
    submitter_gov_agency VARCHAR(300),
    submitter_gov_agency_type VARCHAR(50),
    last_name VARCHAR(100),
    modification_date TIMESTAMP WITH TIME ZONE,
    submitter_org VARCHAR(200),
    phone VARCHAR(40),
    posted_date TIMESTAMP WITH TIME ZONE NOT NULL,
    postmark_date TIMESTAMP WITH TIME ZONE,
    reason_withdrawn VARCHAR(1000),
    received_date TIMESTAMP WITH TIME ZONE,
    restriction_reason VARCHAR(1000),
    restriction_reason_type VARCHAR(20),
    state_province_region VARCHAR(100),
    comment_subtype VARCHAR(100),
    comment_title VARCHAR(500),
    is_withdrawn BOOLEAN DEFAULT FALSE,
    postal_code VARCHAR(20)
);

-- =========================================
-- LINKS TABLE
-- =========================================
-- Stores links for corresponding cfr parts; references cfr parts

CREATE TABLE IF NOT EXISTS links (
    title VARCHAR(50),
    cfrpart VARCHAR(50),
    link VARCHAR(2000) UNIQUE,
    PRIMARY KEY (title, cfrpart)
);

-- =========================================
-- CFR PARTS TABLE
-- =========================================
-- Stores cfr part numbers for corresponding documents; references documents
-- cfrPart and frDocNum will be null at table creation & are retrieved from federal reserve & inserted into the table at the first query

CREATE TABLE IF NOT EXISTS cfrparts (
    frdocnum VARCHAR(50) NOT NULL,
    title VARCHAR(50) NOT NULL,
    cfrpart VARCHAR(50) NOT NULL,
    PRIMARY KEY (frdocnum, title, cfrpart)
);

-- =========================================
-- FEDERAL REGISTER DOCUMENTS TABLE
-- =========================================
-- Stores FR-native document metadata keyed by Federal Register document number.

CREATE TABLE IF NOT EXISTS federal_register_documents (
    document_number VARCHAR(50) NOT NULL PRIMARY KEY,
    document_id VARCHAR(50),
    document_title TEXT,
    document_type VARCHAR(50),
    abstract TEXT,
    publication_date DATE,
    effective_on DATE,
    docket_ids TEXT[],
    agency_id VARCHAR(20),
    agency_names TEXT[],
    topics TEXT[],
    significant BOOLEAN,
    regulation_id_numbers TEXT[],
    html_url VARCHAR(2000),
    pdf_url VARCHAR(2000),
    json_url VARCHAR(2000),
    start_page INTEGER,
    title VARCHAR(50),
    cfrpart VARCHAR(50),
    end_page INTEGER
);

-- =========================================
-- USERS TABLE
-- =========================================
-- Stores users authenticated via Google OAuth

CREATE TABLE IF NOT EXISTS users (
    email VARCHAR(320) NOT NULL PRIMARY KEY,
    name VARCHAR(200) NOT NULL
);

-- =========================================
-- COLLECTIONS TABLE
-- =========================================
-- Stores collections (folders) created by users.

CREATE TABLE IF NOT EXISTS collections (
    collection_id SERIAL PRIMARY KEY,
    user_email VARCHAR(320) NOT NULL REFERENCES users(email),
    collection_name VARCHAR(200) NOT NULL
);

-- =========================================
-- COLLECTION DOCKETS TABLE
-- =========================================
-- Stores which dockets belong to which collection

CREATE TABLE IF NOT EXISTS collection_dockets (
    collection_id INT NOT NULL REFERENCES collections(collection_id) ON DELETE CASCADE,
    docket_id VARCHAR(50) NOT NULL REFERENCES dockets(docket_id),
    PRIMARY KEY (collection_id, docket_id)
);

-- =========================================
-- DOWNLOAD JOBS TABLE
-- =========================================
-- Tracks async download requests submitted by users.
-- status: 'pending' | 'processing' | 'ready' | 'failed'
-- s3_path: set once the archive is uploaded to S3 (NULL until ready)
-- expires_at: used by prune_expired_download_jobs to clean up old records

CREATE TABLE IF NOT EXISTS download_jobs (
    job_id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
    user_email VARCHAR(320) NOT NULL REFERENCES users(email),
    docket_ids TEXT[] NOT NULL,
    format VARCHAR(20) NOT NULL DEFAULT 'zip',
    include_binaries BOOLEAN NOT NULL DEFAULT FALSE,
    status VARCHAR(20) NOT NULL DEFAULT 'pending',
    s3_path VARCHAR(2000),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW(),
    expires_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW() + INTERVAL '7 days'
);

-- =========================================
-- ADMINS TABLE
-- =========================================
-- Stores admin users who can manage authorized users
-- Hardcoded to professor only
-- Must exist in users table first due to foreign key reference

CREATE TABLE IF NOT EXISTS admins (
    email VARCHAR(320) NOT NULL PRIMARY KEY REFERENCES users(email),
    name VARCHAR(200) NOT NULL
);

-- =========================================
-- AUTHORIZED USERS TABLE
-- =========================================
-- Stores users authorized to access dev.mirrulations.org
-- Managed by admin to grant access without going through Google OAuth
-- Users can be removed by deleting their row from this table

CREATE TABLE IF NOT EXISTS authorized_users (
    email VARCHAR(320) NOT NULL PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    authorized_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
);
